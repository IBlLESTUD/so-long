#include "../header/libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*res;
	t_list	*new;

	if (!lst)
		return (NULL);
	res = NULL;
	while (lst)
	{
		new = ft_lstnew(f(lst -> content));
		if (!new)
		{
			ft_lstdelone(new, (*del));
			return (NULL);
		}
		ft_lstadd_back(&res, new);
		lst = lst -> next;
	}
	return (res);
}
