#include "../header/libft.h"

void	ft_lstclear(t_list **lst, void (*del)(void*))
{
	t_list	*tpm;
	t_list	*list;

	if (*lst)
	{
		list = *lst;
		while (list)
		{
			tpm = list -> next;
			ft_lstdelone(list, del);
			list = tpm;
		}
		*lst = NULL;
	}
}
